
public class IntegerToWrapper {

	public static void main(String[] args) {
		// Boxing
		int a = 50;
		Integer aOBJ = new Integer(a);
		
		
		double b = 5;
		Double bOBJ = new Double (b);
		
		char a = 'a';
		Character aOBJ = new Character (a);
		
		//Unboxing
		
		Integer num1 = 10;

		Boolean status = true;

		Byte b = 1;
		
	}

}
